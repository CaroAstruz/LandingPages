const SUPPORTED_LANGS = ['fr', 'en'];
const DEFAULT_LANG = 'fr';

const BASE = import.meta.env.BASE_URL; // e.g. '/LandingPages/' or '/'

/**
 * Detect language from URL path, accounting for base path.
 */
export function detectLang() {
  const path = window.location.pathname;
  const basePath = BASE.endsWith('/') ? BASE : BASE + '/';
  const relative = path.startsWith(basePath) ? path.slice(basePath.length) : path;
  const match = relative.match(/^(fr|en)(\/|$)/);
  return match ? match[1] : null;
}

/**
 * Apply all translations for the given language.
 */
export function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  // Update <html lang>
  document.documentElement.lang = lang;

  // Update meta
  document.title = dict["meta.title"];
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', dict["meta.description"]);

  // Update textContent elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) el.textContent = dict[key];
  });

  // Update innerHTML elements (for HTML content like <br/>, <em>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key]) el.innerHTML = dict[key];
  });

  // Update placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) el.setAttribute('placeholder', dict[key]);
  });

  // Update lang switcher active state
  document.querySelectorAll('.lang-link').forEach(link => {
    link.classList.toggle('active', link.dataset.lang === lang);
  });
}

/**
 * Redirect to root → /fr if no lang in path.
 * Returns the current lang.
 */
export function initLang() {
  let lang = detectLang();

  if (!lang) {
    // No lang prefix found → redirect to base/fr
    const hash = window.location.hash;
    window.location.replace(BASE + DEFAULT_LANG + hash);
    return DEFAULT_LANG;
  }

  applyTranslations(lang);
  return lang;
}

/**
 * Get current language.
 */
export function getCurrentLang() {
  return detectLang() || DEFAULT_LANG;
}

/**
 * Build a path with the current language prefix.
 */
export function langPath(hash) {
  const lang = getCurrentLang();
  return '/' + lang + (hash || '');
}
